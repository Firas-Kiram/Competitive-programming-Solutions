const ll MAXN = 1e6 + 100;
ll fac[MAXN + 1];
ll inv[MAXN + 1];

ll exp(ll x, ll n, ll m) {
    x %= m;
    ll res = 1;
    while (n > 0) {
        if (n % 2 == 1) { res = res * x % m; }
        x = x * x % m;
        n /= 2;
    }
    return res;
}

void factorial() {
    fac[0] = 1;
    for (int i = 1; i <= MAXN; i++) { fac[i] = fac[i - 1] * i % MOD; }
}

void inverses() {
    inv[MAXN] = exp(fac[MAXN], MOD - 2, MOD);
    for (int i = MAXN; i >= 1; i--) { inv[i - 1] = inv[i] * i % MOD; }
}

ll choose(int n, int r) { return fac[n] * inv[r] % MOD * inv[n - r] % MOD; }

void solve() {
    ll n , k;
    cin >> n >> k;
    vector <ll> v(n) , a(n);
    for(int i = 0 ; i < n ; i ++)
        cin >> v[i]; 
    sort(all(v));
    ll p = k / 2;
    for(int i = 0 ; i < n ; i ++ ){
        a[i] = mul(choose(i , p) , choose(n - i - 1, p));
    }
    ll res = 0;
    for(int i = p ; i < n - p ; i ++){
        res += mul(a[i] , v[i]);
    }
    res %= MOD;
    cout << res << ln;
}

